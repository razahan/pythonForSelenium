# For Loop

#Print 1.....10 numbers using "for loop"

for i in range(1,11):
    print(i)

print("Done!!!")

# 1...20 even number
for i in range(0, 21, 2):
    print(i)

print("Done!!!")

# 1...20 odd number
for i in range(1, 21, 2):
    print(i)

print("Done!!!")

# 1...10 descnding order
for i in range(10, 0, -1):
    print(i)

